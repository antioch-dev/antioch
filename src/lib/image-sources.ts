// Helper function to get random image from a category
export function getRandomImage(category: keyof typeof ImageSources) {
  const images = Object.values(ImageSources[category])
  return images[Math.floor(Math.random() * images.length)] ?? ImageSources.hero.background
}

// Helper function to get image with specific dimensions
export function getImageWithDimensions(url: string, width: number, height: number): string {
  return url.replace(/w=\d+&h=\d+/, `w=${width}&h=${height}`)
}
